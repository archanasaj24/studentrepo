package com.assignment5;

public class Swapping {

	public static void main(String[] args) 
	{
		int num1=45;
		int num2=54;
		int temp;
		System.out.println("Number before swap:\n"+num1+" "+num2);
		temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("Number after swap:\n"+num1+" "+num2);

	}

}
